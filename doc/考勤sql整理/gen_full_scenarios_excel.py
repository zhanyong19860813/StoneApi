def main():
    script_dir = os.path.dirname(os.path.abspath(__file__))
    out_path = os.path.join(script_dir, "Data_full_scenarios_对照表.xlsx")

    wb = Workbook()
    ws = wb.active
    ws.title = "全场景对照表"

    # 标题行
    ws.append(["考勤全场景日结果对照表"])
    ws.merge_cells("A1:O1")
    ws["A1"].font = Font(bold=True, size=14)
    ws["A1"].alignment = Alignment(horizontal="center")
    ws.append([])

    # 表头
    ws.append(HEADERS)
    header_row = ws.max_row
    for col in range(1, len(HEADERS) + 1):
        cell = ws.cell(row=header_row, column=col)
        cell.font = Font(bold=True)
        cell.alignment = Alignment(horizontal="center", wrap_text=True)

    # 数据行
    for row in DATA:
        # row: 日期, 班次, 班次说明, 是否跨天, 刷卡时间, 异常情况, exp_attStatus, exp_attStatus1, exp_attStatus2, exp_attStatus3, exp_attLate, exp_attEarly, exp_attAbsenteeism, 备注
        s1 = "-" if row[7] == "" else row[7]
        s2 = "-" if (len(row) > 8 and row[8] == "") else (row[8] if len(row) > 8 else "-")
        s3 = "-" if (len(row) > 9 and row[9] == "") else (row[9] if len(row) > 9 else "-")
        ws.append([
            "A8425", row[0], row[1], row[2], row[3],
            row[4] if isinstance(row[4], str) else ", ".join(str(x) for x in row[4]),
            row[5], row[6], s1, s2, s3, row[10], row[11], row[12], row[13]
        ])

    # 列宽
    ws.column_dimensions["A"].width = 8
    ws.column_dimensions["B"].width = 12
    ws.column_dimensions["C"].width = 18
    ws.column_dimensions["D"].width = 10
    ws.column_dimensions["E"].width = 10
    ws.column_dimensions["F"].width = 35
    ws.column_dimensions["G"].width = 18
    ws.column_dimensions["H"].width = 14
    ws.column_dimensions["I"].width = 14
    ws.column_dimensions["J"].width = 14
    ws.column_dimensions["K"].width = 14
    ws.column_dimensions["L"].width = 12
    ws.column_dimensions["M"].width = 12
    ws.column_dimensions["N"].width = 16
    ws.column_dimensions["O"].width = 55

    # 说明 sheet
    ws2 = wb.create_sheet("说明", 0)
    ws2["A1"] = "attStatus：0=正常 1=迟到 2=早退 3=漏打卡"
    ws2["A2"] = "attAbsenteeism：0=否 1=是(旷工)"
    ws2["A4"] = "班次说明：两次卡 / 四次卡 / 六次卡"
    ws2["A5"] = "是否跨天：不跨天 / 上班跨天(前日上班) / 下班跨天(次日下班)"

    wb.save(out_path)
    print(f"已生成: {out_path}")

if __name__ == "__main__":
    main()
