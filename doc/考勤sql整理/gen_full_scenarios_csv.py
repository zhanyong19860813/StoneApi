HEADERS = ["工号", "日期", "班次", "班次说明", "是否跨天", "刷卡时间", "异常情况", "预期attStatus", "预期attStatus1", "预期attStatus2", "预期attStatus3", "预期attLate", "预期attEarly", "预期attAbsenteeism", "备注"]

def main():
    script_dir = os.path.dirname(os.path.abspath(__file__))
    out_path = os.path.join(script_dir, "Data_full_scenarios_对照表.xlsx")
    csv_path = os.path.join(script_dir, "Data_full_scenarios_对照表.csv")

    # 优先尝试生成 xlsx
    try:
        from openpyxl import Workbook
        from openpyxl.styles import Font, Alignment

        wb = Workbook()
        ws = wb.active
        ws.title = "全场景对照表"
        ws.append(["考勤全场景日结果对照表"])
        ws.merge_cells("A1:O1")
        ws["A1"].font = Font(bold=True, size=14)
        ws["A1"].alignment = Alignment(horizontal="center")
        ws.append([])
        ws.append(HEADERS)
        for cell in ws[ws.max_row]:
            cell.font = Font(bold=True)

        for row in DATA:
            card_time = row[4] if isinstance(row[4], str) else ", ".join(str(x) for x in row[4])
            ws.append(["A8425", row[0], row[1], row[2], row[3], card_time, row[5], row[6], row[7], row[8], row[9], row[10], row[11], row[12], row[13]])

        ws.column_dimensions["A"].width = 8
        ws.column_dimensions["B"].width = 12
        ws.column_dimensions["C"].width = 18
        ws.column_dimensions["D"].width = 10
        ws.column_dimensions["E"].width = 10
        ws.column_dimensions["F"].width = 35
        ws.column_dimensions["G"].width = 18
        ws.column_dimensions["O"].width = 55

        ws2 = wb.create_sheet("说明", 0)
        ws2["A1"] = "attStatus：0=正常 1=迟到 2=早退 3=漏打卡"
        ws2["A2"] = "attAbsenteeism：0=否 1=是(旷工)"
        ws2["A4"] = "班次说明：两次卡 / 四次卡 / 六次卡"
        ws2["A5"] = "是否跨天：不跨天 / 上班跨天(前日上班) / 下班跨天(次日下班)"

        wb.save(out_path)
        print(f"已生成 Excel: {out_path}")
        return
    except ImportError:
        pass

    # 无 openpyxl 时生成 CSV（Excel 可直接打开）
    with open(csv_path, "w", newline="", encoding="utf-8-sig") as f:
        writer = csv.writer(f)
        writer.writerow(["考勤全场景日结果对照表"])
        writer.writerow(HEADERS)
        for row in DATA:
            card_time = row[4] if isinstance(row[4], str) else ", ".join(str(x) for x in row[4])
            writer.writerow(["A8425", row[0], row[1], row[2], row[3], card_time, row[5], row[6], row[7], row[8], row[9], row[10], row[11], row[12], row[13]])
    print(f"已生成 CSV（Excel 可直接打开）: {csv_path}")
    print("提示：若需 .xlsx，请执行 pip install openpyxl 后重新运行本脚本")

if __name__ == "__main__":
    main()
